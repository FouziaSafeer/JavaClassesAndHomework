package replitPractice;

public class replit69 {
    public static void main(String[] args) {
        for (int i = 7; i >= 2; i--) {

            for (int j = 1; j <= i; j++) {
                // if(j==)
                System.out.print(j + " ");
            }
            System.out.println();
        }


        for (int k = 1; k < 8; k++) {
            for (int l = 1; l <= k; l++) {
                System.out.print(l + " ");
            }
            System.out.println();

        }
    }
}

