public class NestedLoops2 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) { //out for loop
            for (int j = 0; j < 5; j++) {// innner for loop
                System.out.println("i="+i+ " J ="+j);

            }
            System.out.println("***********************");
        }
    }
}
